package testandoFor36;

public class TestandoFor36 {
    public static void main(String[] Args) {

        for (int i=0; i<3; i++) {

            System.out.print("Estou contando dentro o for :D (i) ");

            System.out.println(i);

            for (int j=0; j<3; j++) {

                System.out.print("Estou contando dentro do 2o for (j) :D ");

                System.out.println(j);

                System.out.print("Estou calculando dentro do 2o for (i) :D ");

                System.out.println(i);

            }

        }

    }
}
