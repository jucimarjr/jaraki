public class For5 {
    public static void main(String[] Args) {

        int n;
		n = 500000;

        int x;
		x = 0;

        for (int i=0; i <= n; i++) {

            System.out.print("Estou contando e calculando dentro o for :D ");

            System.out.println(i);

            x = x + i;

            System.out.println(x);
        }

    }
}
