public class TestandoWhile5{
  public static void main(String[] args){
    
    int i;
    i = 1;
    int n;
    n = 20;
    int x;
    x = 0;

    while(i <= n){

	System.out.print("Estou contando e calculando dentro do while :D ");

	System.out.println(i); 

	x = x + i;

	System.out.println(x); 

	i++; 
    } 
  }
}
